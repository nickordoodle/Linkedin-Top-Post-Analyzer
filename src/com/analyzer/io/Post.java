class Post {
	// Field
	private int id;
	private String textBody;
	private String hashTags;
	private int likes;
	private int comments;
	private int views;


	// Constructor

	public Post(int id, String textBody, String hashTags, int likes, int comments, int views) {
		this.id = id;
		this.textBody = textBody;
		this.hashTags = hashTags;
		this.likes = likes;
		this.comments = comments;
		this.views = views;
	}

	// Method

	// Accessor

}